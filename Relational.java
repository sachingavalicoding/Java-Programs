public class Relational {
    public static void main(String [] args ) {
	System.out.println(true == true );//true
	System.out.println(true != false);//true
	System.out.println(10 >= 11);// false
	System.out.println(20 <= 21);// true 
	System.out.println('a' >= 'A'); // true 97 > 65
	System.out.println('B' >= 66 ); // true 
	System.out.println(22.0/7.0);/// double value so deciamls numbers is more 
	System.out.println(22.0f / 7.0f);// float value so deciams after numbers is less 
	System.out.println(22.0 / 7 );// converted to double 
	System.out.println(22/7.0);
	System.out.println(22/7.0f);
	System.out.println(22.0f/7);


  }
}
